    @Override
    public void actionPerformed(ActionEvent e) {
        Object evt = e.getSource();
        
        if(evt.equals(btnUsuarios)){
            panelPeliculas.setVisible(false);
            panelSeries.setVisible(false);
            panelUsuarios.setVisible(true);
            
            
            contenedor.add(panelUsuarios);
            contenedor.validate();
            deshabilitarBotones();
            
        }else if(evt.equals(btnPeliculas)){
            panelUsuarios.setVisible(false);
            panelSeries.setVisible(false);
            panelPeliculas.setVisible(true);
            
            contenedor.add(panelPeliculas);
            contenedor.validate();
            deshabilitarBotones();
        }else if(evt.equals(btnSeries)){
            panelUsuarios.setVisible(false);
            panelPeliculas.setVisible(false);
            panelSeries.setVisible(true);
            
            contenedor.add(panelSeries);
            contenedor.validate();
            deshabilitarBotones();
        }
    }
